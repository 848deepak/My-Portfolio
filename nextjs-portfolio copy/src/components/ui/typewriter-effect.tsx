// New component specifically for cycling through different texts
export function TypewriterEffectCycling({
  text,
  className,
  cursorClassName = "",
  onComplete,
  ...props
}: {
  text: string;
  className?: string;
  cursorClassName?: string;
  onComplete?: () => void;
  // ...other prop types
}): JSX.Element {
  const [displayText, setDisplayText] = useState("");
  const [currentIndex, setCurrentIndex] = useState(0);

  useEffect(() => {
    setDisplayText("");
    setCurrentIndex(0);
  }, [text]);

  useEffect(() => {
    if (currentIndex < text.length) {
      const timer = setTimeout(() => {
        setDisplayText(prev => prev + text[currentIndex]);
        setCurrentIndex(prev => prev + 1);
      }, 100); // Adjust speed here

      return () => clearTimeout(timer);
    } else if (onComplete) {
      // Call onComplete when typing is done
      setTimeout(onComplete, 500);
    }
  }, [currentIndex, text, onComplete]);

  return (
    <span className={cn("inline-flex items-center", className)}>
      <span className={"dark:text-white text-black"}>
        {displayText}
      </span>
      <motion.span
        initial={{ opacity: 0 }}
        animate={{ opacity: 1 }}
        transition={{ duration: 0.8, repeat: Infinity, repeatType: "reverse" }}
        className={cn(
          "inline-block align-baseline rounded-sm w-[2px] h-[1.1em] bg-blue-500 ml-1",
          cursorClassName
        )}
      ></motion.span>
    </span>
  );
} 